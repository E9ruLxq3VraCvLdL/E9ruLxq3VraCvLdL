    d&uuml;rften die offizielle Meinung der Reformkommission widerspiegeln. Taqizadeh 
    geht allerdings davon aus, der erste Tag dieser Zeitrechnung sei der 17. M&auml;rz 
    622 gewesen, eine Anschauung, die auch die &uuml;brigen Mitglieder der Kommission 
    ihren Berechnungen zugrunde gelegt haben d&uuml;rften. Im Jahr 622 fiel die 
    Tag- und Nachtgleiche auf den 18. M&auml;rz gegen 13.30 Uhr Isfahaner Ortszeit, 
    also bereits in den Nachmittag. Der 1. Farwardin des Jahres 1 muss demzufolge 
    entsprochen haben dem 19. M&auml;rz 622 julianischen Stils. Seltsam mutet 
    es schon an, dass das Gesetz von 1925 zu der zentralen Frage, wie der Jahresbeginn 
    zu definieren sei, keine Stellung bezieht.</p>
  <p align="left">Wenn nun aber der Jahresanfang astronomisch bestimmt wird, kann 
    es streng genommen keine Schaltregel geben. Welches Jahr 365 Tage und welches 
    Jahr 366 Tage hat, ergibt sich aus den Berechnungen der Sonnenbahn. In der 
    Praxis kann man nat&uuml;rlich f&uuml;r sehr lange Zeit den oben erw&auml;hnten 
    Zyklus von 128 Jahren mit 31 Schalttagen nutzen, bei dem einmal nach 29 Jahren 
    und drei mal nach je 33 Jahren ein f&uuml;nfj&auml;hriges Schaltintervall 
    eingeschoben wird. Da der gregorianische Kalender einen anderen Schaltzyklus 
    hat, verschieben sich beide Kalender laufend gegeneinander. Vor 1925 fiel 
    innerhalb von vier Jahren das iranische Neujahr zwei mal auf den 22. M&auml;rz 
    und zwei mal auf den 21. M&auml;rz. Das Jahr 1304 (1925/26) leitete ein f&uuml;nfj&auml;hriges 
    Schaltintervall ein, so dass ab 1309 innerhalb von vier Jahren Nowruz drei 
    mal auf den 21. M&auml;rz und einmal auf den 22. M&auml;rz fiel. Der n&auml;chste 
    Schaltsprung folgte auf das f&uuml;nfj&auml;hrige Intervall 1337 (1958/59) 
    bis 1342 (1963/64). Bis 1375 (1996/97) war das iranische Neujahr immer am 
    21. M&auml;rz, um von da ab f&uuml;r 33 Jahre innerhalb von vier Jahren dreimal 
    auf den 21. M&auml;rz und einmal auf den 20. M&auml;rz zu fallen. Es wird 
    sich weiter verschieben und gegen Ende des 21. Jahrhunderts immer auf den 
    20. M&auml;rz zu liegen kommen. Im Jahre 2100 hat der gregorianische Kalender 
    einen Schaltsprung, der bewirkt, dass ab 2100 Nowruz f&uuml;r einige Jahre 
    wieder st&auml;ndig am 21. M&auml;rz sein wird.</p>
