    neue Jahr feierlich zu begr&uuml;ssen. In Kalendern ist sekundengenau dieser 
    Moment verzeichnet, im Fernsehen l&auml;uft eine Uhr, die ebenfalls die Sekunden 
    z&auml;hlt. Und dennoch ist dieser Tag nicht automatisch der Neujahrstag, 
    unabh&auml;ngig davon, ob man den Tagesbeginn am Vorabend bei Sonnenuntergang 
    oder um Mitternacht ansetzt. In der Regel beginnt derzeit das Jahr erst am 
    folgenden Tag.</p>
