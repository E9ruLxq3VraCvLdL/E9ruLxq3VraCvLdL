    Gesetz geht offensichtlich von einem astronomisch fixierten Kalender aus, 
    bei dem der Neujahrstag immer auf den Tag des Fr&uuml;hlings&auml;quinoktium 
    zu fallen hat. Wie bereits im Zusammenhang mit dem Kalender Dschelal ed Din´s 
    angesprochen, ist die Frage, an welchem Tag der Fr&uuml;hling beginnt, auch 
    abh&auml;ngig davon, auf welche Zeit man heute den Tagesanfang setzt. Wenn 
    Walther Hinz schreibt, &quot;die persischen Wochentage, die jeweils um 18 
    Uhr beginnen (nicht wie in Deutschland um 24 oder 0 Uhr),...&quot; , so ist 
    dies stark vereinfachend. Die Uhrzeit im Iran entspricht der heute auf der 
    ganzen Welt g&uuml;ltigen Norm. Sie beginnt um Mitternacht mit 0.00 Uhr, der 
    Tag beginnt also ebenfalls um Mitternacht. Andererseits ist der Iran ein islamisches 
    Land. Daher spielt der islamische Kalender eine grosse Rolle. Die meisten 
    Feiertage im Iran sind religi&ouml;se Fest- oder Trauertage, die mit dem Vorabend 
    beginnen. Daher wird h&auml;ufig in der Bev&ouml;lkerung der Abend als Tagesbeginn 
