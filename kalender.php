    des Freitags&quot; bezeichnet den Abend und die Nacht vom Donnerstag zum Freitag, 
    w&auml;hrend &quot;Freitag Abend&quot; den Abend und die Nacht vom Freitag 
    zum Samstag meint. Ein Tagesbeginn zur Zeit des Sonnenaufgangs scheint heute 
    nicht mehr bekannt zu sein.</p>
