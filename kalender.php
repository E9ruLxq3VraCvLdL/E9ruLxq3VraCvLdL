    Sonne im jeweiligen Tierkreiszeichen. Die Ekliptik, die scheinbare Bahn der 
    Sonne um die Erde, wird bekanntlich in 12 Teile zu je 30 Grad unterteilt, 
    beginnend mit dem Fr&uuml;hlingspunkt. Wie lange die Sonne braucht, um so 
    einen Abschnitt zu durchmessen, h&auml;ngt von ihrer Bahngeschwindigkeit ab. 
