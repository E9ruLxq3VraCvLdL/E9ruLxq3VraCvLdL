  <h3 align="left"><font size="2">Zusammenfassung</font></h3>
  <p align="left">Der heutige iranische Kalender l&auml;sst sich in seinen Grundz&uuml;gen 
    zur&uuml;ckverfolgen bis in die Zeit der Ach&auml;meniden. Seine Grundeinheit 
    bildet das Sonnenjahr, das unterteilt wird in 12 Monate. Die ersten sechs 
    Monate haben je 31 Tage, die folgenden f&uuml;nf Monate je 30 Tage. Der letzte 
    Monat z&auml;hlt im Gemeinjahr 29 und im Schaltjahr 30 Tage. Dies ergibt eine 
    Jahresl&auml;nge von 365 Tagen im Gemeinjahr und von 366 Tagen im Schaltjahr.</p>
  <p align="left">Das iranische Jahr ist rein astronomisch definiert. Es beginnt 
    und endet jeweils in dem Moment, in dem der Sonnenmittelpunkt die aufsteigende 
    Ekliptik schneidet. Der Tag, auf den dieses Ereignis f&auml;llt, ist der erste 
    Tag des neuen Jahres, sofern dieser astronomische Fr&uuml;hlingsbeginn vor 
    12.00 Uhr Mittags iranischer Zeit eintritt, ansonsten wird Neujahr auf den 
    folgenden Tag verschoben. Diese Regelung bewirkt, dass zwar grunds&auml;tzlich 
    jedes vierte Jahr ein Schaltjahr ist, in mehr oder weniger regelm&auml;ssigen 
    Abst&auml;nden jedoch ein f&uuml;nfj&auml;hriges Schaltintervall eingeschoben 
    werden muss. Einige der gebr&auml;uchlichen Umrechnungstabellen tragen diesem 
    Tatbestand nur unzureichend Rechnung. Daher werden an anderer Stelle neu &uuml;berarbeitete 
    <a href="iranneutab.html">Tabellen</a> wiedergegeben, die auf einfache Art 
    eine korrekte Umwandlung iranischer Daten in den abendl&auml;ndischen Kalender 
    und umgekehrt erm&ouml;glichen.</p>
