  <h3 align="left"><font size="2">Der altiranische Kalender</font></h3>
  <p align="left"> Mit der Verbreitung der Religion Zarathustras im Iran gewann 
    auch der zoroastrische Kalender immer gr&ouml;ssere Bedeutung unter den Persern. 
    Gegen Ende des 6. Jahrhunderts, sp&auml;testens jedoch in der Mitte des 5. 
    Jahrhunderts vor Christus l&ouml;ste er die bis dahin bestehende babylonische 
    Zeitrechnung ab und wurde zum offiziellen Reichskalender.</p>
  <p align="left">Diesem altiranischen Kalender lag das Sonnenjahr zugrunde, unterteilt 
    in 12 Monate zu je 30 Tagen und f&uuml;nf Zusatztagen, den Epagomenen. Die 
    Namen der Monate und die Bezeichnungen der Tage im Monat - eine Besonderheit 
    des iranischen Kalenders - sind &uuml;berliefert. Sie sind identisch mit denen 
    des heutigen iranischen Kalenders. </p>
  <p align="left">So gut wir auch &uuml;ber derartige Einzelheiten unterrichtet 
    sind, &uuml;ber den inneren Aufbau dieser Zeitrechnung gibt es noch einige 
    Unklarheiten. Nach Autoren aus islamischer Zeit handelte es sich um ein Wandeljahr 
    von 365 Tagen, &auml;hnlich dem &auml;gyptischen Jahr, ohne jede Schaltung. 
    Die Jahre wurden nach den Regierungsjahren der Herrscher gez&auml;hlt, der 
    Jahresanfang lag urspr&uuml;nglich in der Zeit der Tag- und Nachtgleiche des 
    Fr&uuml;hlings. Da dieses altiranische Jahr um fast sechs Stunden k&uuml;rzer 
    war als das astronomische Sonnenjahr, verschob sich allm&auml;hlich der Jahresanfang 
    gegen den Fr&uuml;hlingspunkt. In ungef&auml;hr 120 Jahren summierten sich 
    diese j&auml;hrlichen Abweichungen auf etwa 30 Tage, was einem ganzen Monat 
    entspricht. Zumindest f&uuml;r den kultischen Bereich war es aber wichtig, 
    dass sich die Jahrpunkte, nach denen sich die religi&ouml;sen Feste richteten, 
    nicht allzu weit vom wirklichen Sonnenstand entfernten. Daher musste man ca. 
    alle 120 Jahre einen zus&auml;tzlichen 13. Monat einschalten. Dies geschah 
    dergestalt, dass ein Monat zweimal gez&auml;hlt wurde. Allerdings kam diese 
    Schaltung nur im religi&ouml;sen Kalender zur Anwendung, die gew&ouml;hnliche 
    b&uuml;rgerliche Jahresrechnung war hiervon nicht betroffen.</p>
  <p align="left">In der Theorie kann man sich dieses Verfahren ungef&auml;hr 
    folgendermassen vorstellen: Unterstellt man, dass im (zur&uuml;ckgerechneten) 
