    <strong>Der iranische Lyriker und Maler Sohrab 
    Sepehri</strong></p>
  <p align="left"><img src="http://www.iran-infos.de/images/sepehri3.gif" align="left" vspace="10" hspace="10"> 
    <br>
    Sohrab Sepehri war 1928 in Kashan als Spross einer bekannten Gelehrtenfamilie 
    zur Welt gekommen und starb 1980 in Tehran.<br>
    Er studierte an der Hochschule für K&uuml;nste in Tehran und unternahm mehrere 
    Reisen nach Europa.<br>
    Er war nicht nur Lyriker, sondern auch Maler. Die Stärke seiner Gedichte sind 
    deren Bilder. <br>
    Er fühlte sich als ein Teil Natur und sie macht er zum Inhalt des Gedichtes 
    "Das Wasser", das man geradezu als ein ökologisches Manifest nennen kann, 
    obwohl zu der Zeit, als es verfasst würde, Umweltschutz noch kein Thema war, 
    das eine grössere Anzahl von Menschen interessiert hätte:
  <p align="left">&nbsp; 
  <p align="left"> <strong>Das Wasser</strong><br>
    Trübt mir das Wasser nicht: <br>
    Nimmt an, flussabwärts trinkt eine Taube davon, <br>
    Im fernen Hain wäscht ein Stieglitz seine Flügel Oder im Dorf wird gerade 
    ein Krug gefüllt. <br>
    <br>
    Trübt mir das Wasser nicht: <br>
    Vielleicht strömt dies fliessendes Nass zum Fuss einer Silberpappel,<br>
    um ihr die Trauer aus dem Herzen zu waschen. <br>
    Die Hand eines armen Mannes hat vielleicht ein Stückchen trocken Brot hineingetrunkt. 
    <br>
    Eine schöne Frau trat ans Ufer des Flusses; <br>
    <br>
  <div align="left">
