    Er zeigte mit dem Finger eine Pappel und sagte:<br>
    &quot;Noch vor dem Baum ist der Gartenweg, gr&uuml;ner als Gottes Traum,<br>
    Und darin ist die Liebe, so blau wie die Fl&uuml;gel der Aufrichtigkeit.<br>
    Du gehst bis zum Ende dieses Wegs, der zur Reife f&uuml;hrt,<br>
    Dann biegst du ab zur Blume der Einsamkeit<br>
    Zwei Schritte vor der Blume<br>
    Stehst du am Fusse der Font&auml;ne der ewigen Mythologie der Ackerkrume.<br>
    Und dich ergreift eine durchsichtige Furcht.<br>
    In der fliessenden Offenherzigkeit des Raumes<br>
    h&ouml;rst du ein Rauschen:<br>
    Du siehst ein Kind,<br>
    Das eine hohe Zypresse hinaufgeklettert ist, um ein<br>
    K&uuml;cken aus dem Licht des Nestes zu nehmen,</p>
  <p align="left">Und du fragst es:<br>
    &quot;Wo ist das Haus des Freundes?&quot;</p>
  <p align="left"><br>
  </p>
  <hr align="center" width="100" noshade>
  <p align="left">Sepehri war ein genuiner Vertreter östlicher Kultur. Er hat 
    eine durchaus religiöse Auffassung vom Leben und der Welt, aber diese Religiösität 
    ist Teil seiner Kultur, nicht Teil einer von Staats wegen verordneten orthodoxen 
    Religion. Seine pantheistische Naturschwärmerei ist in diesem Sinne sogar 
    ketzerisch. Sehr deutlich wird das, wenn er in einem anderen Gedicht schreibt:<br>
    "Ich bin Moslem. Mein Mekka ist eine rote Rose." <br>
    Sepehri erkrankte 1979 an Blutkrebs und starb im folgenden Jahr in Tehran.<br>
