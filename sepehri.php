  <p align="left">Trübt mir das Wasser nicht: <br>
    Verdoppelt ist ihr schönes Angesicht. <br>
    Wie köstlich ist dies Nass! <br>
    Wie klar ist dieser Fluss! <br>
    Welche Reinheit die Leute im Dorf dort oben haben! <br>
    Ihre Quellen sollen sprudeln, ihre Kühe reichlich Milch geben! <br>
    Ich habe ihr Dorf nicht gesehn, Doch sicher wurzeln ihre Hecken in den Spuren 
    von Gottes Fuss. <br>
    Dort erleuchtet das Mondlicht die Weite des Wortes. <br>
    Sicher sind die Mauern dort oben im Dorf nicht so hoch. <br>
    Seine Bewohner wissen, wie der Klatschmohn blüht. Gewiss ist blau dort blau. 
    <br>
    Und öffnet sich dort eine Knopse, so wissen's dort die Bewohner. <br>
    Was für ein Dorf muss das sein! <br>
    Ich wünsche ihnen Musik auf den Wegen ihrer Gärten. <br>
    An der Quelle des Flusses verstehen die Leute das Wasser. <br>
    <br>
    Sie haben es nicht getrübt, auch ihr, <br>
    Trübt mir das Wasser nicht. </p>
  <hr align="center" width="100" noshade>
  <p align="left"> Vielleicht dürfen wir in dem obigen Gedicht einen Nachklang 
    der zoroastrischen Ehrfrucht vor den Elementen und dem daraus folgenden Bemühen 
    um deren Reinhaltung sehen.<br>
    <br>
    Religionphilosophisch kann man auch das Gedicht "Und" interpretieren. Dieser 
    Text des sowohl vom Sufismus als auch vom Buddhismus beeinflussten Dichters, 
    lädt ein, darüber nachzudenken, wozu wir leben. Er ist als Zwiegespräch aufgebaut, 
    in dem einer der Partner fragt, der andere antwortet. Die Fragen aber gehen 
    weiter, auch wenn die Antworten schon verstummt sind. Das Gedicht bleibt offen, 
    der Titel "Und" zeigt, dass dies nur die eine Seite ist und dass es noch eine 
    andere gibt. Somit weist dieses Gedicht über unser vergängliches Dasein hinaus:<br>
  </p>
  <p align="left"><br>
    <b>Und</b> <br>
    ''Knopsen sind wir eines Traums.'' <br>
    ''Knospen eines Traums? Und werden wir je blühen?'' <br>
    ''Eines Tages, doch ohne, dass ein Blüttenblatt sich rührt.'' <br>
    ''Hier?'' <br>
    ''Ja, hier in diesem Jammertal.'' <br>
    ''Aber die Dunkelheit, die Einsamkeit?'' <br>
    ''Doch. Schönheit blüht für sich allein.'' <br>
    ''Wer wird je kommen, sich an uns zu freuen, uns zu riechen?'' <br>
    ''....'' <br>
    ''Von einem Winde fortgeweht zu werden?'' <br>
    ''....'' <br>
    ''Und wieder zu vergehn?'' <br>
    <br>
  </p>
  <hr align="center" width="100" noshade>
  <p align="left">Surrealistische Anklängen sowie eine ausgesprochen malerische 
    Sehweise finden wir in ''Adresse''.<br>
    Scheinbar in einem Atemzug geschrieben, ist das Gedicht verhältnismässig kurz. 
    Es beginnt und endet mit der Frage:<br>
    ''Wo ist das Haus des Freundes?'' <br>
    Es ist die Frage nach dem Sinn unseres Daseins: Die Suche nach einem lebenden 
    und geliebten Gegenüber. Dazwischen finden wir eine Beschreibung nicht des 
    direkten Weges zum Ziel, sondern zu einem, der den Weg zu diesem Ziel zeigen 
    könnte. Dieser Kunstgriff erlaubt es Sepehri, das Gedicht mit derselben Frage 
    zu beschliessen, mit der er es eröffnet hat. <br>
  </p>
  <p align="left"><br>
    <b>Die Adresse</b> <br>
    "Wo ist das Haus des Freundes?"<br>
